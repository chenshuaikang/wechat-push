package com.imcsk.entity;

/**
 * @Description one数据实体
 * @Author csk
 * @Date 2022/10/18
 */
public class OneDataBean {
    private String oneid;
    private String word;
    private String wordfrom;
    private String imgurl;
    private String imgauthor;
    private String date;

    public String getOneid() {
        return oneid;
    }

    public void setOneid(String oneid) {
        this.oneid = oneid;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getWordfrom() {
        return wordfrom;
    }

    public void setWordfrom(String wordfrom) {
        this.wordfrom = wordfrom;
    }

    public String getImgurl() {
        return imgurl;
    }

    public void setImgurl(String imgurl) {
        this.imgurl = imgurl;
    }

    public String getImgauthor() {
        return imgauthor;
    }

    public void setImgauthor(String imgauthor) {
        this.imgauthor = imgauthor;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
